#include "dbsql_config.h"

#ifndef NO_SYSTEM_INCLUDES
#include <sys/types.h>
#endif

/*
 * memcmp --
 *
 * PUBLIC: #ifndef HAVE_MEMCMP
 * PUBLIC: int memcmp __P((const void *, const void *, size_t));
 * PUBLIC: #endif
 */
#ifndef HAVE_MEMCMP
int
memcmp(s1, s2, n)
	char *s1, *s2;
	size_t n;
{
	if (n != 0) {
		unsigned char *p1 = (unsigned char *)s1,
			      *p2 = (unsigned char *)s2;
		do {
			if (*p1++ != *p2++)
				return (*--p1 - *--p2);
		} while (--n != 0);
	}
	return (0);
}
#endif
